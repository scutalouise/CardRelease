package com.sanploy.card.report.pojo;


public class DotReportCondition {

	private String startDate;

	private String endDate;

	private String dotId;
	
	private String dotReportType;

	/**
	 * @return the startDate
	 */
	public String getStartDate() {
		return startDate;
	}

	/**
	 * @param startDate the startDate to set
	 */
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	/**
	 * @return the endDate
	 */
	public String getEndDate() {
		return endDate;
	}

	/**
	 * @param endDate the endDate to set
	 */
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	/**
	 * @return the dotReportType
	 */
	public String getDotReportType() {
		return dotReportType;
	}

	/**
	 * @param dotReportType the dotReportType to set
	 */
	public void setDotReportType(String dotReportType) {
		this.dotReportType = dotReportType;
	}

	/**
	 * @return the dotId
	 */
	public String getDotId() {
		return dotId;
	}

	/**
	 * @param dotId the dotId to set
	 */
	public void setDotId(String dotId) {
		this.dotId = dotId;
	}

}
